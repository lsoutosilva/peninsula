                            <h3 class="post-title">
                                Hormônio pode substituir a insulina para os diabéticos.
                            </h3>
                            <div class="post-thumb">													
                                <img src="../wp-content/uploads/2012/09/diabetes.jpg" alt="Latest Health Lessons">
                            </div><!-- end of post thumb -->

                        </header>																						
                        <p>

                            Até agora, acreditava-se que os diabéticos não conseguiriam sobreviver sem insulina. Recentemente, contudo, têm surgido evidências de que o hormônio leptina, que participa do metabolismo dos lipídeos, pode substituir as injeções de insulina. Em um estudo suíço publicado na revista científica "Cell Metabolism", camundongos diabéticos sobreviveram sem insulina.
                             
                            A equipe liderada por Roberto Coppari da Universidade de Genebra injetou leptina diretamente no cérebro dos camundongos incapazes de produzir o hormônio regulador da glicemia, insulina. Isto permitiu que identificassem as células no cérebro responsáveis pelo efeito de redução da glicemia da leptina.
                             
                            Além disso, os pesquisadores definiram os tecidos que, estimulados pela leptina, absorveram açúcar do sangue. Foram eles: o fígado, músculos específicos e o tecido adiposo marrom. No futuro, eles podem ser alvo de novas terapias. "Graças a essas constatações, existem alternativas à insulina", disse Coppari. A leptina parece até mesmo ser um adversário melhor do hormônio de elevação da glicemia, o glucagon, reduzindo a glicose sanguínea de modo mais preciso. Ela não desencadeia hipoglicemia, reduz os depósitos de gordura e, de quebra, limita o apetite.
                             
                        </p>

