                                Barriga grande aumenta o risco de morte em sobreviventes de ataque cardíaco.
                            </h3>
                            <div class="post-thumb">													
                                <img src="../wp-content/uploads/2012/09/barriga.jpg" alt="Latest Health Lessons">
                            </div><!-- end of post thumb -->

                        </header>																						
                        <p>

                            Apresentado no Congresso de 2013 da Sociedade Europeia de Cardiologia.<br/>
                             
                            Ter a barriga grande aumenta o risco de morte em sobreviventes de ataque cardíaco, de acordo com pesquisa apresentada no ESC, realizado em agosto e setembro em Amsterdã. 
                             
                            Foi observado que tanto os pacientes magros (IMC <22) e os muito obesos (IMC >/=35) tinham um risco maior de morte após cinco anos: + 41% e +65%, respectivamente. Estar no quartil mais alto de circunferência da cintura (ou seja, ter uma barriga grande devido ao excesso de peso) também foi um indicador de mortalidade mais alta após cinco anos (+44%). O risco após cinco anos foi mais baixo para IMC entre 22 e 35.
                             
                        </p>

