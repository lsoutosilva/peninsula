                            <h3 class="post-title">
                                PROTEÍNA ACELERA O CÂNCER AO INVÉS DE INIBI-LO
                            </h3>
                            <div class="post-thumb">													
                                <img src="../wp-content/uploads/2012/09/cancer.jpg" alt="Latest Health Lessons">
                            </div><!-- end of post thumb -->

                        </header>																						
                        <p>

                            Uma proteína, que se imaginava inibir o crescimento tumoral, tem função oposta, de acordo com um estudo norte-americano. O estudo, publicado na revista científica "Science Signaling", revelou que a enzima angiomotina estimula o crescimento inicial de tumores.
                             
                            A angiomotina é responsável pela migração de células e pela disseminação de outros tipos celulares, principalmente durante o início do crescimento de novos vasos sanguíneos. Diversos estudos concluíram que a proteína inibia o câncer. Mas ao analisar culturas celulares e modelos animais, pesquisadores do The Scripps Research Science Institute (TSRI) em Jupiter (Flórida) descobriram que o oposto é verdadeiro.
                             
                            Ao invés disso, a angiomotina tem papel importante para o oncogene Yap (Yes-associated-Protein) que é super-expresso em vários tipos de câncer. O Yap precisa da angiomotina para garantir o crescimento de novos tumores. A proteína é ativa dentro do núcleo celular e não, como se pensava até agora, somente na membrana celular.
                            
                            Os pesquisadores esperam que tais achados possam abrir novos caminhos para o tratamento.
                             
                        </p>

