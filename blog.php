                            <h3 class="post-title">
                                Mitos e verdades na alimentação de pessoas com diabetes.
                            </h3>
                            <div class="post-thumb">													
                                <img src="../wp-content/uploads/2012/09/alimentos.jpg" alt="Latest Health Lessons">
                            </div><!-- end of post thumb -->

                        </header>																						
                        <p>

                            Alguns dos mitos e verdades na alimentação de pessoas com diabetes são discutidos a seguir:
                            <br/>
                            MITO: Todos os indivíduos portadores de diabetes devem seguir uma dieta específica, com restrição de calorias e nutrientes para atingir melhor controle glicêmico.
                            <br/>VERDADE: Não existe uma dieta específica para indivíduos diabéticos, pois suas necessidades nutricionais são semelhantes aos da população em geral. A nutricionista avaliará o estado nutricional para definir as necessidades, de forma individual e de acordo com o objetivo do tratamento, de calorias, macro e micronutrientes, utilizando os parâmetros semelhantes ao da população geral.
                            <br/><br/>
                            MITO: As frutas como banana, uva, caqui, manga e melancia, famosas por “aumentarem muito” a glicemia do paciente diabético, devem ser excluídas da alimentação. 
                            <br/>VERDADE: Todas as frutas contem açúcares chamados frutose e glicose, alem disso todas possuem importante quantidade de fibras, vitaminas e minerais.  As  frutas citadas acima, não precisam ser excluídas e devem ser consumidas em quantidades adequadas e distribuídas ao longo do dia. Não só essas frutas, mas todas as outras frutas quando consumidas em excesso na mesma refeição podem provocar o aumento da glicemia e descontrole do diabetes.
                            <br/><br/>
                            MITO: Os indivíduos diabéticos devem comer pão amanhecido ou torrado porque não prejudicará o controle do diabetes.
                            <br/>VERDADE: Qualquer tipo de pão, seja ele francês, integral, etc., são uma importante fonte de carboidrato da dieta. Esse nutriente é o que mais afeta a glicemia, pois 100% dele é convertido em glicose (açúcar). Assim, não importa a forma de preparo, um pão terá sempre uma quantidade determinada de carboidrato, estando ele torrado ou amanhecido. Portanto consuma a quantidade orientada pela nutricionista e da forma que mais gostar.
                            <br/><br/>
                            MITO: A pipoca é um alimento perigoso para as pessoas com diabetes e não deve ser consumida.
                            <br/>VERDADE:  A pipoca é um alimento rico em fibras que contribui com a saciedade e melhores níveis de  glicemia. Alem disso, é de baixo custo. Por ser fonte de carboidrato, deve ser substituída pelo pão, onde: 01 xícara de pipoca “pronta” equivale a ½ pão francês ou 01 fatia de pão de forma.
                            <br/>MITO:  Portadores de diabetes não podem comer beterraba. 
                            <br/>VERDADE: os diabéticos, não só podem como devem consumir a beterraba, pois é classificada como hortaliças e não como carboidrato, alem de ser ótima fonte de fibras, vitaminas e minerais. Pode fazer parte da dieta orientada pela nutricionista.
                            <br/><br/>
                            MITO: Comer antes de dormir engorda, principalmente  se for carboidrato.
                            <br/>VERDADE: A quantidade de calorias ingeridas é que faz com que o peso se eleve. Não é o consumo exclusivo de carboidratos que promove o ganho de peso e sim o consumo de energia maior que o gasto. 
                            <br/><br/>
                            MITO: Indivíduos com diabetes não podem comer arroz e feijão.
                            <br/>VERDADE: Muito pelo contrario, não só podem como devem. Sabemos que o feijão é um dos alimentos mais ricos em fibras solúveis, além do amido resistente (outro tipo de fibra). A presença desse tipo de fibra torna a digestão mais lenta, o que proporciona menor elevação da glicose no sangue, além de aumentar a saciedade. 

                            MITO: Produtos “Diet” são feitos para diabéticos e podem ser consumidos a vontade.
                            <br/>VERDADE: De acordo com a Agência Nacional de Vigilância Sanitária (ANVISA) podem ser chamados de alimentos “diet” aqueles que são isentos de algum nutriente que, nem sempre é o carboidrato, podendo ser  gordura ou sódio, por exemplo. Podemos citar como exemplo o chocolate: algumas marcas apresentam maior teor de gordura e pouca ou nenhuma diferença em relação aos carboidratos, ou seja, nem sempre o chocolate diet é a melhor escolha. Para boas escolhas sempre devemos comparar os rótulos dos alimentos. Em caso de dúvida,  sempre consulte um nutricionista.
                            <br/>MITO: Portadores de diabetes só devem comer pequenas quantidades de alimentos ricos em carboidratos, como batata, arroz, macarrão, etc... 
                            <br/>VERDADE: Alimentos ricos em carboidratos são parte de um plano alimentar saudável e são essenciais como fornecedores de energia. O que é importante é o tamanho da porção. Pães integrais, cereais, massas, arroz e legumes ricos em amido, como batatas, inhame, ervilha e milho podem ser incluídos nas refeições e lanche, desde que não se exagere no tamanho da porção em cada uma das refeições.
                            <br/> MITO: Indivíduos com diabetes não podem comer doces ou chocolates. <br/>
                            <br/>VERDADE: Se ingeridos como parte de um plano alimentar saudável, os doces e sobremesas pode ser consumido por pessoas com diabetes. Eles não são mais "proibidos" para indivíduos diabéticos. A dica para doces é não consumir em grande quantidade e guardá-los para ocasiões especiais.

