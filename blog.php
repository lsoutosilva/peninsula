                            <h3 class="post-title">
                                Bebidas adoçadas com açúcar.
                            </h3>
                            <div class="post-thumb">													
                                <img src="../wp-content/uploads/2012/09/bebidas.jpg" alt="Latest Health Lessons">
                            </div><!-- end of post thumb -->

                        </header>																						
                        <p>

                            O consumo de bebidas adoçadas com açúcar foi mostrado, em grande parte em populações americanas, como sendo responsável por aumentar a incidência de diabetes tipo 2. Um estudo publicado, recentemente, na revista Diabetologia, teve por objetivo avaliar a associação do consumo de bebidas doces (sumos e néctares, refrigerantes adoçados com açúcar e refrescos adoçados artificialmente) com a incidência de diabetes tipo 2 em adultos europeus.
                             
                            Os autores estabeleceram um estudo de caso-coorte incluindo 12.403 casos incidentes de diabetes tipo 2 e uma subcoorte estratificada de 16.154 participantes selecionados de oito coortes europeias participantes do estudoEuropean Prospective Investigation into Cancer and Nutrition (EPIC). Após as exclusões, o tamanho final da amostra incluiu 11.684 casos incidentes e uma subcoorte de 15.374 participantes. Modelos de riscos de regressão proporcionais de Cox (modificados para o estudo de caso-coorte) e meta-análises de efeitos aleatórios foram utilizados para estimar a associação entre o consumo de bebidas doces (obtida a partir de questionários dietéticos validados) e incidência de diabetes tipo 2.
                             
                            Nos modelos ajustados, um incremento diário de 336 g (12 oz) no consumo de bebidas adoçadas com açúcar e adoçadas artificialmente foi associado com HRs para diabetes tipo 2 de 1,22 (IC 95% 1,09, 1,38) e 1,52 (IC 95% 1,26, 1,83), respectivamente. Após ajuste para o consumo de energia e IMC, a associação de refrigerantes adoçados com açúcar e diabetes tipo 2 persistiu (HR 1,18, IC 95% 1,06, 1,32), mas a associação de bebidas adoçadas artificialmente tornou-se estatisticamente não significativa (HR 1,11; IC 95% 0,95, 1,31). O consumo de suco e néctar não foi associado com a incidência de diabetes tipo 2.
                             
                            Os autores concluíram que o estudo corrobora a associação entre o aumento da incidência de diabetes tipo 2 e o alto consumo de refrigerantes açucarados em adultos europeus.
                             
                            Consumption of sweet beverages and type 2 diabetes incidence in European adults: results from EPIC-InterAct - Diabetologia; 2013 Apr 26. [Epub ahead of print]

                        </p>

